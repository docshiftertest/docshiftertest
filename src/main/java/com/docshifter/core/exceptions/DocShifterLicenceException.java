package com.docshifter.core.exceptions;

public class DocShifterLicenceException extends Exception {


    public DocShifterLicenceException() {
    }

    public DocShifterLicenceException(String arg0) {
        super(arg0);
    }


    public DocShifterLicenceException(Throwable arg0) {
        super(arg0);
    }


    public DocShifterLicenceException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

}
